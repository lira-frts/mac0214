#include <bits/stdc++.h>
using namespace std;

using ll = long long;

void solve(){
    ll a, b; cin >> a >> b;
    cout << min(a, b) << ' ' << max(a, b) << '\n';
}

int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);

    ll t = 1;
    cin >> t;
    
    while(t--) solve();
}
