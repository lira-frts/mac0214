#include <bits/stdc++.h>
using namespace std;

using ll = long long;

using vll = vector<ll>;
using pll = pair<ll, ll>;
using vpll = vector<pll>;
using vvll = vector<vll>;
using vvpll = vector<vpll>;

#define F first
#define S second
#define pb push_back
#define all(x) x.begin(),x.end()

void solve(){
    ll n; cin >> n;
    ll maximo = -1, minimo = 1e18;
    for(ll i = 0; i < n; i++){
        ll x; cin >> x;
        maximo = max(maximo, x); minimo = min(minimo, x);
    }
    cout << (maximo - minimo) * (n - 1) << '\n';
}

int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);

    ll t = 1;
    cin >> t;
    
    while(t--) solve();
}
